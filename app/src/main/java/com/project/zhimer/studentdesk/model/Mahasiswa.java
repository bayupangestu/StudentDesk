package com.project.zhimer.studentdesk.model;

public class Mahasiswa {

    private String nim;
    private String nama;
    private String prodi;
    private String pembimbing;
    private String jalurMasuk;
    private String statusAkademik;
    private String alamat;
    private String kota;
    private String kodePos;
    private String telp;
    private String phone;
    private String email;
    private String tahun;
    private String lulus;


    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getProdi() {
        return prodi;
    }

    public void setProdi(String prodi) {
        this.prodi = prodi;
    }

    public String getPembimbing() {
        return pembimbing;
    }

    public void setPembimbing(String pembimbing) {
        this.pembimbing = pembimbing;
    }

    public String getJalurMasuk() {
        return jalurMasuk;
    }

    public void setJalurMasuk(String jalurMasuk) {
        this.jalurMasuk = jalurMasuk;
    }

    public String getStatusAkademik() {
        return statusAkademik;
    }

    public void setStatusAkademik(String statusAkademik) {
        this.statusAkademik = statusAkademik;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getKota() {
        return kota;
    }

    public void setKota(String kota) {
        this.kota = kota;
    }

    public String getKodePos() {
        return kodePos;
    }

    public void setKodePos(String kodePos) {
        this.kodePos = kodePos;
    }

    public String getTelp() {
        return telp;
    }

    public void setTelp(String telp) {
        this.telp = telp;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public String getLulus() {
        return lulus;
    }

    public void setLulus(String lulus) {
        this.lulus = lulus;
    }
}
