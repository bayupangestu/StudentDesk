package com.project.zhimer.studentdesk.model;

public class Quran {

    private String informasi;
    private String tanggalTest;
    private String jumlahPeserta;

    private String tanggal;
    private String score;
    private String status;

    public String getInformasi() {
        return informasi;
    }

    public void setInformasi(String informasi) {
        this.informasi = informasi;
    }

    public String getTanggalTest() {
        return tanggalTest;
    }

    public void setTanggalTest(String tanggalTest) {
        this.tanggalTest = tanggalTest;
    }

    public String getJumlahPeserta() {
        return jumlahPeserta;
    }

    public void setJumlahPeserta(String jumlahPeserta) {
        this.jumlahPeserta = jumlahPeserta;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
