package com.project.zhimer.studentdesk.model;

public class Nilai {

    private String kodeMK;
    private String namaMK;
    private Integer sks;
    private Integer angka;
    private String huruf;
    private Integer bobot;

    public String getKodeMK() {
        return kodeMK;
    }

    public void setKodeMK(String kodeMK) {
        this.kodeMK = kodeMK;
    }

    public String getNamaMK() {
        return namaMK;
    }

    public void setNamaMK(String namaMK) {
        this.namaMK = namaMK;
    }

    public Integer getSks() {
        return sks;
    }

    public void setSks(Integer sks) {
        this.sks = sks;
    }

    public Integer getAngka() {
        return angka;
    }

    public void setAngka(Integer angka) {
        this.angka = angka;
    }

    public String getHuruf() {
        return huruf;
    }

    public void setHuruf(String huruf) {
        this.huruf = huruf;
    }

    public Integer getBobot() {
        return bobot;
    }

    public void setBobot(Integer bobot) {
        this.bobot = bobot;
    }
}
